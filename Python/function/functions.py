#function = A block of reusable code that is used to perform a specific task
#          place () after the function name to invoke it

def happyBirthday(name, age): # define a function
    print(f"Happy Birthday to {name}!")
    print("Happy Birthday to you!")
    print(f"Happy Birthday, dear {name}!")
    print(f"Happy Birthday to the {age} years old mammal!")
    print()

happyBirthday("John", 30)
happyBirthday("Jane", 25)
happyBirthday("Joe", 21)